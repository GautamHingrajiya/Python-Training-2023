# Write a Python program to test whether a passed letter is a vowel or not.

letter1=input("Enter any Latter to Check Vowel or not : ")

if letter1 == "a" or letter1 == "A" or letter1 == "e" or letter1 == "E" or letter1 == "i" or letter1 == "I" or letter1 == "o" or letter1 == "O" or letter1 == "u" or letter1 == "U" :
    print("Entered Latter is Vowel")
else:
    print("Entered Latter is not Vowel")